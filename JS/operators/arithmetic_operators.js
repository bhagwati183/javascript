// Post Increment:-After assigning the value to the variable, the value is incremented..
var i=3;
console.log(i++);// here i=3+1 
console.log(i);//4

// another example of post increment.
var j=5;
j++;//5+1=6
console.log(j);// therefore j=6.

//Pre-increment:-Before assigning the value to the variable, the value is incremented by 1.
var k=15;
console.log(++k);//here 1+15

//OR
var k=16;
++k;
console.log(k);